// Check for MetaMask when page is loaded
window.addEventListener('load', function() {
    if (typeof window.ethereum !== 'undefined') {
        console.log("MetaMask detected!")
    }
    else {
        console.log('mm not available')
        this.alert("Install MetaMask to use Fragmints")
    }
})

// Connect to MetaMask
const mmEnable = document.getElementById('mm-connect');
mmEnable.onclick = async () => {
    await ethereum.request({ method: 'eth_requestAccounts'})
    mmEnable.innerHTML = ethereum.selectedAddress.substring(0,5) + "..." + 
        ethereum.selectedAddress.substring(38)
	// display number of minted tokens
	let amountMinted = await fragmints.methods.amountMinted().call();
	let mintCount = document.getElementById('fm-mint-count')
	mintCount.innerHTML = amountMinted;
	// display the total amount of fragmints
	let totalSupply = await fragmints.methods.totalSupply().call()
	let remaining = document.getElementById('fm-mint-remaining')    
	remaining.innerHTML = totalSupply - amountMinted;
	// display the total amount of fragmints
	let contractFragmints = await fragmints.methods.totalFragmints().call();
	let fragmintCount = document.getElementById('fm-fragmint-count')    
    fragmintCount.innerHTML = contractFragmints;
}

const fmMint = document.getElementById('fm-mint-button')
fmMint.onclick = async () => {
	let amountMinted = await fragmints.methods.amountMinted().call();
	if (amountMinted >= fragmints.totalSupply) {
		alert("None available, sorry.")
	} else {
		var id = await fragmints.methods.mintOriginal().send({from: ethereum.selectedAddress})
		// display number of minted tokens
		let amountMinted = await fragmints.methods.amountMinted().call();
		let mintCount = document.getElementById('fm-mint-count')
		mintCount.innerHTML = amountMinted;
		// display the total amount of fragmints
		let totalSupply = await fragmints.methods.totalSupply().call()
		let remaining = document.getElementById('fm-mint-remaining')    
		remaining.innerHTML = totalSupply - amountMinted;
		// display the total amount of fragmints
		let contractFragmints = await fragmints.methods.totalFragmints().call();
		let fragmintCount = document.getElementById('fm-fragmint-count')    
		fragmintCount.innerHTML = contractFragmints;
		console.log(id.events)
		alert(`Token ID ${id.events.originalMinted.returnValues['tokenId']} minted!`)
	}
}

const fmFragmint = document.getElementById('fm-fragmint-button')
fmFragmint.onclick = async () => {

	let tokenId = document.getElementById('fm-fragmint-id').value
	let amount = document.getElementById('fm-fragmint-amount').value 

	var id = await fragmints.methods.fragmint(tokenId, amount).send( {from: ethereum.selectedAddress} )
	// display number of minted tokens
	let amountMinted = await fragmints.methods.amountMinted().call();
	let mintCount = document.getElementById('fm-mint-count')
	mintCount.innerHTML = amountMinted;
	// display the total amount of fragmints
	let totalSupply = await fragmints.methods.totalSupply().call()
	let remaining = document.getElementById('fm-mint-remaining')    
	remaining.innerHTML = totalSupply - amountMinted;
	// display the total amount of fragmints
	let contractFragmints = await fragmints.methods.totalFragmints().call();
	let fragmintCount = document.getElementById('fm-fragmint-count')    
	fragmintCount.innerHTML = contractFragmints;
	console.log(id.events)
	alert(`Token ID ${tokenId} was fragminted into ${amount} tokens with ID ${id.events.originalFragminted.returnValues['tokenId']}!`)
}

